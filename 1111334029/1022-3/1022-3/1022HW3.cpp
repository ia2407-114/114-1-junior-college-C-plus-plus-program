#include <stdio.h>
int main() {
	double p, r, yl, aims;
	int year = 0;
	double mt = 0;
	printf("輸入本金:\n");
	scanf_s("%lf", &p);
	printf("輸入年利率:\n");
	scanf_s("%lf", &r);
	printf("輸入月底定期存入金額:\n");
	scanf_s("%lf", &yl);
	printf("輸入存款目標\n");
	scanf_s("%lf", &aims);
	mt = r / 12;
	while (p < aims)
	{
		year++;
		for (int i = 0; i < 12; i++) {
			p += yl;
			p = p + (p * mt);
		}
		printf("第%d年本利合為%lf\n", year, p);
	}
	printf("共需要%d年才能達到目標\n", year);
}